<h3>Universell utforming (UU)</h3>
<p>Universell utforming (UU) er et generelt begrep innen ikt, arkitektur, tjeneste- og produktutvikling og samfunnsplanlegging som går ut på at man ikke skal diskriminere noen på bakgrunn av funksjonsevne, altså unngå diskriminering av ulike brukergrupper.
Offentlige og private virksomheter i Norge skal følge forskriften om universell utforming av Ikt- løsninger (ifølge forskrift om universell utforming av IKT-løsninger, 2012 §2) og må ha oppfylt 35 av 61 kriterier i standarden WCAG 2.0 så lenge de har en nettbasert løsning som går under følgende punkter:
1-	Nettløsningen blir brukt til å tilby eller informere om tjenester.
2-	Virksomheten er rettet mot innbyggere i Norge.
3-	Nettsiden er ny eller oppdatert etter 1. juli 2014.

</p>



<h3>Brukertesting</h3>
<p>
For å sikre reell UU kan vi gjennomføre brukertesting:
Målet med brukertesting er å se hvor enkelt produktet er i praksis. En brukertest gir deg tilbakemelding på om noe ikke fungerer eller om noe oppleves som ulogisk. Man får blant annet vite hva som er utfordrende for brukeren slik at man får mulighet til å øke kvaliteten på produktet. På denne måten kan man spare penger og unngå at kunden går videre til en konkurrent. 
Brukertesting skal helst gjennomføres flere ganger under designprosessen, ved å gjøre dette kontinuerlig sparer man kostander i utviklingsfasen og etter lansering. Hvem skal gjennomføre brukertesting?
flere personer går gjennom nettsiden, helst også personer med nedsatte funksjonsevner, og tester om nettsiden er brukervennlig og hvorvidt innholdet er forståelig.  

Nav.no får godkjent av oss på alle kriteriene om universell utforming. 
Nav har en generelt teknisk god nettside, den kan allikevel være litt mer oversiktlig da mange av
 undersidene har litt for mye informasjon på ett sted og forskjellig design på 
 noen av dem. Dette kan fremstå som forvirrende for mange, spesielt mennesker som ikke er vandt til å bruke data.</p>


